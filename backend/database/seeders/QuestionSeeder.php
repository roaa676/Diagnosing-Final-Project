<?php

namespace Database\Seeders;

use Illuminate\Database\Seeder;
use App\Models\LearningDifficulty;
use App\Models\Question;

class QuestionSeeder extends Seeder
{
    public function run()
    {
        // مسح الأسئلة القديمة لتجنب التكرار
        Question::truncate();

        // ==========================================
        // 1. أسئلة عسر القراءة (Dyslexia) - 8 أسئلة
        // ==========================================
        $dyslexia = LearningDifficulty::where('name_ar', 'عسر القراءة')->first();
        
        if ($dyslexia) {
            $dyslexiaQuestions = [
                'هل يواجه الطفل صعوبة في تذكر أسماء الحروف أو أصواتها المألوفة؟',
                'هل يقرأ الطفل الكلمات ببطء شديد ويتهجأ بصعوبة مقارنة بأقرانه؟',
                'هل يخلط أو يعكس الطفل الحروف المتشابهة (مثل ب/ت، d/b) أثناء القراءة أو الكتابة؟',
                'هل يواجه الطفل صعوبة في فهم واستيعاب القصة بعد قراءتها رغم قدرته على نطق الكلمات؟',
                'هل يتجنب الطفل القراءة بصوت مرتفع أمام الآخرين أو يشعر بالارتباك عند القيام بذلك؟',
                'هل يواجه الطفل صعوبة في نطق الكلمات الطويلة أو المعقدة بشكل صحيح؟',
                'هل يلاحظ تعثر الطفل في تذكر ترتيب أيام الأسبوع أو الشهور أو الحروف الهجائية؟',
                'هل يبذل الطفل مجهوداً ذهنياً كبيراً في الكتابة لدرجة أنه ينسى محتوى ما يكتبه؟',
            ];
            
            foreach ($dyslexiaQuestions as $index => $text) {
                Question::create([
                    'learning_difficulty_id' => $dyslexia->id, 
                    'question_text' => $text, 
                    'order' => $index + 1
                ]);
            }
        }

        // ==========================================
        // 2. أسئلة عسر الحساب (Dyscalculia) - 8 أسئلة
        // ==========================================
        $dyscalculia = LearningDifficulty::where('name_ar', 'عسر الحساب')->first();
        
        if ($dyscalculia) {
            $dyscalculiaQuestions = [
                'هل يجد الطفل صعوبة في ربط الرقم المكتوب بقيمته الفعلية (مثل ربط الرقم 5 بخمس تفاحات)؟',
                'هل يستمر الطفل في الاعتماد على أصابعه للعد في مسائل بسيطة رغم تجاوزه السن المتوقع لذلك؟',
                'هل يواجه صعوبة بالغة في تذكر الحقائق الرياضية الأساسية (مثل 2+2=4 أو جداول الضرب)؟',
                'هل يعاني الطفل من صعوبة في فهم مفاهيم الوقت (قراءة الساعة) أو الاتجاهات (يمين/يسار)؟',
                'هل يجد الطفل صعوبة في تقدير نواتج العمليات الحسابية بشكل تقريبي؟',
                'هل يخلط الطفل بين العلامات الحسابية (مثل الخلط بين علامة + و علامة ×)؟',
                'هل يواجه الطفل تحدياً في ترتيب الأرقام تصاعدياً أو تنازلياً بشكل صحيح؟',
                'هل يواجه الطفل صعوبة في فهم المسائل اللفظية وتحويلها إلى معادلات رقمية؟',
            ];
            
            foreach ($dyscalculiaQuestions as $index => $text) {
                Question::create([
                    'learning_difficulty_id' => $dyscalculia->id, 
                    'question_text' => $text, 
                    'order' => $index + 1
                ]);
            }
        }
    }
}